package com.rf1m.web.image2css;

public class CSSClass {
	private String name;
	private String body;

	public CSSClass(){
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
}
